function personalIntroduction(name, height, favoriteColor) {
  return "My name is " + name + ", and my height is " + height + ", and my favorite color is " + favoriteColor;
}

console.log(personalIntroduction("Bob", "72", "red"));
console.log(personalIntroduction("Samantha", "65", "orange"));
console.log(personalIntroduction("Christopher", "69", "green"));
console.log(personalIntroduction("Tamera", "63", "blue"));
